package com.googlecode.g2re.domain;

/**
 * Describes the data type of an element.
 * @author Brad Rydzewski
 */
public enum DataType {

    /**
     * Maps to java.lang.String.
     */
    STRING,
    /**
     * Maps to java.lang.Integer.
     */
    INTEGER,
    /**
     * Maps to java.util.Date.
     */
    DATE,
    /**
     * Maps to java.lang.Double.
     */
    DOUBLE,
    /**
     * Maps to java.lang.Float.
     */
    FLOAT
}
