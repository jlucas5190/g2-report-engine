package com.googlecode.g2re.domain;

/**
 * Defines the order in which a list is ordered.
 * @author Brad Rydzewski
 */
public enum SortOrder {
    /**
     * Represents an organization of data in increasing order, a-Z, 0-9.
     */
    Ascending,
    /**
     * Represents an organization of data in decreasing order, 9-0, z-A.
     */
    Descending
}
