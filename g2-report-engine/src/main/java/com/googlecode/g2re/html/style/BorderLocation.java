package com.googlecode.g2re.html.style;

/**
 * Sets the side(s) where the border is rendered. Options include
 * All, top, bottom, left, right
 * @author Brad Rydzewski
 */
public enum BorderLocation {
    /**
     * Renders border on all sides of element.
     */
    all,
    /**
     * Renders border on the top side of element.
     */
    top,
    /**
     * Renders border on the bottom side of element.
     */
    bottom,
    /**
     * Renders border on the left side of element.
     */
    left,
    /**
     * Renders border on the right side of element.
     */
    right
}
